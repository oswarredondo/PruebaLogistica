package ar.com.unisolutions.dto;

public class MercanciaSat {

	String clavemcia;
	String descripcionmcia;
	String similares;
	
	public MercanciaSat() {
		super();
	}
	
	public String getClavemcia() {
		return clavemcia;
	}
	public void setClavemcia(String clavemcia) {
		this.clavemcia = clavemcia;
	}
	public String getDescripcionmcia() {
		return descripcionmcia;
	}
	public void setDescripcionmcia(String descripcionmcia) {
		this.descripcionmcia = descripcionmcia;
	}
	public String getSimilares() {
		return similares;
	}
	public void setSimilares(String similares) {
		this.similares = similares;
	}
	
}
