package com.innovargia.autodoc.dto;

public class FiltrosReportesIndicadores {

	String centro_operativo;
	String tipo_entrega;
	String mes_recoleccion;
	String fecha_inicio;
	String fecha_fin;
	
	public String getCentro_operativo() {
		return centro_operativo;
	}
	public void setCentro_operativo(String centro_operativo) {
		this.centro_operativo = centro_operativo;
	}
	public String getTipo_entrega() {
		return tipo_entrega;
	}
	public void setTipo_entrega(String tipo_entrega) {
		this.tipo_entrega = tipo_entrega;
	}
	public String getMes_recoleccion() {
		return mes_recoleccion;
	}
	public void setMes_recoleccion(String mes_recoleccion) {
		this.mes_recoleccion = mes_recoleccion;
	}
	public String getFecha_inicio() {
		return fecha_inicio;
	}
	public void setFecha_inicio(String fecha_inicio) {
		this.fecha_inicio = fecha_inicio;
	}
	public String getFecha_fin() {
		return fecha_fin;
	}
	public void setFecha_fin(String fecha_fin) {
		this.fecha_fin = fecha_fin;
	}
}
